package de.tu_berlin.indoornavigation;

import android.support.v7.widget.RecyclerView;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import java.util.List;

import de.tu_berlin.indoornavigation.entities.CompanionRequest;

/**
 * {@link RecyclerView.Adapter} that can display a {@link CompanionRequest} and makes a call to the
 * specified {@link CompanionRequestFragment.OnListFragmentInteractionListener}.
 */
public class MyCompanionRequestRecyclerViewAdapter extends RecyclerView.Adapter<MyCompanionRequestRecyclerViewAdapter.ViewHolder> {

    private final List<CompanionRequest> mValues;
    private final CompanionRequestFragment.OnListFragmentInteractionListener mListener;

    public MyCompanionRequestRecyclerViewAdapter(List<CompanionRequest> items, CompanionRequestFragment.OnListFragmentInteractionListener listener) {
        mValues = items;
        mListener = listener;
    }

    @Override
    public ViewHolder onCreateViewHolder(ViewGroup parent, int viewType) {
        View view = LayoutInflater.from(parent.getContext())
                .inflate(R.layout.fragment_companionrequest, parent, false);
        return new ViewHolder(view);
    }

    @Override
    public void onBindViewHolder(final ViewHolder holder, int position) {
        holder.mItem = mValues.get(position);
        holder.mContentView.setText(mValues.get(position).getFrom().getUsername());

        holder.mView.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                if (null != mListener) {
                    // Notify the active callbacks interface (the activity, if the
                    // fragment is attached to one) that an item has been selected.
                    mListener.onListFragmentInteraction(holder.mItem);
                }
            }
        });
    }

    @Override
    public int getItemCount() {
        return mValues.size();
    }

    public class ViewHolder extends RecyclerView.ViewHolder {
        public final View mView;
        public final TextView mIdView;
        public final TextView mContentView;
        public CompanionRequest mItem;

        public ViewHolder(View view) {
            super(view);
            mView = view;
            mIdView = (TextView) view.findViewById(R.id.id);
            mContentView = (TextView) view.findViewById(R.id.content);
        }

        @Override
        public String toString() {
            return super.toString() + " '" + mContentView.getText() + "'";
        }
    }
}
